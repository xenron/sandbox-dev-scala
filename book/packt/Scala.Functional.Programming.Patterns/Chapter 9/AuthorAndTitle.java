class AuthorAndTitle {
	private final String authName;
	private final String title;

	public AuthorAndTitle(String authName, String title) {
		super();
		this.authName = authName;
		this.title = title;
	}

	public String getAuthName() {
		return authName;
	}

	public String getTitle() {
		return title;
	}
};

